#include "ft_printf.h"

char	*ft_strchr(const char *s, int c)
{
	char	*p;

	if (!s)
		return (NULL);
	p = (char *)s;
	while (*p != (char)c)
	{
		if (!*p)
			return (NULL);
		p++;
	}
	return (p);
}
